     */
    public function store(RegisterRequest $request)
    {   
        $request->merge([
            'password'=>Hash::make($request->password),
        ]);
        $store = User::create($request->all());
        if($store){
                Auth::loginUsingId($store->id);
                return redirect()->route('home.index');
        }
        
    }

